    {
      t = t[1:9]
    } else if (all(t[1:7] - t[2:8]<0))
    {
      t = t[1:8]
    } else if (all(t[1:6] - t[2:7]<0))
    {
      t = t[1:7]
    } else if (all(t[1:5] - t[2:6]<0))
    {
      t = t[1:6]
    } else if (all(t[1:4] - t[2:5]<0))
    {
      t = t[1:5]
    } else if (all(t[1:3] - t[2:4]<0))
    {
      t = t[1:4]
    } else if (all(t[1:2] - t[2:3]<0))
    {
      t = t[1:3]
    } else if (all(t[1:1] - t[2:2]<0))
    {
      t = t[1:2]
    }
    
    t_n = length(t)
     while((t[t_n] > T_01[i] + T_12[i]) & ((t[max(which(t < T_01[i]))] == t[max(which(t < T_01[i] + T_12[i]))]) | (t[min(which(t > T_01[i]))] == t[min(which(t > T_01[i] + T_12[i]))]) |
          ((T_01[i] + T_12[i] + T_23[i]) <= t[min(which(t > T_01[i] + T_12[i]))]))){
      t1 = 0.000001
      t2 = runif(1, t1, 1)
      t3 = runif(1, t1, 2)
      t4 = runif(1, t1, 5)
      t5 = runif(1, t1, 10)
      t6 = runif(1, t1, 12)
      t7 = runif(1, t1, 15)
      t8 = runif(1, t1, 18)
      t9 = runif(1, t1, 20)
      t10 = runif(1, t1, 22)
      t = c(t1, t2, t3, t4, t5, t6, t7, t8, t9, t10)
      if(all(t[1:9] - t[2:10]<0))
