      upper_int2 = min(which(t > T_01[i] + T_12[i]))
      t_type3 = rbind(t_type3, c(t[lower_int1], t[upper_int1], t[lower_int2], t[upper_int2], 
                                 T_01[i] + T_12[i] + T_23[i]))
    }
  }
  return(list(t_type0 = t_type0, t_type1 = t_type1, t_type2 = t_type2, t_type3 = t_type3))
}

sum_all_no_censoring = function(a, t_type0, t_type1, t_type2, t_type3, mc_cores){
  type_1_no_censoring = -sum(sapply(t_type0, function(t) (log(S_01(a, t)))))
  type_2_no_censoring = -sum(unlist(mclapply(1:nrow(t_type1), function(i) (log(f_01(a, (t_type1[i,2]+t_type1[i,1])/2)*
                                                                                 S_12(a,t_type1[i,3]-(t_type1[i,2]+t_type1[i,1])/2))), mc.cores = mc_cores)))
  type_3_no_censoring = -sum(unlist(mclapply(1:nrow(t_type2), function(i) {
    return(log(f_01(a, (t_type2[i,2]+t_type2[i,1])/2)*f_12(a,(t_type2[i,4]+t_type2[i,3]-(t_type2[i,2]+t_type2[i,1]))/2)*S_23(a,t_type2[i,5]-
                                                                                                                               (t_type2[i,4]+t_type2[i,3])/2)))
  },
  mc.cores = mc_cores)))
  type_4_no_censoring = -sum(unlist(mclapply(1:nrow(t_type3), function(i) {
    return(log(f_01(a, (t_type2[i,2]+t_type2[i,1])/2)*f_12(a,(t_type2[i,4]+t_type2[i,3]-(t_type2[i,2]+t_type2[i,1]))/2)*f_23(a,t_type2[i,5]-
                                                                                                                                  (t_type2[i,4]+t_type2[i,3])/2)))
  },
    mc.cores = mc_cores)))
  return(type_1_no_censoring + type_2_no_censoring + type_3_no_censoring + type_4_no_censoring) 
}

sum_all = function(a, t_type0, t_type1, t_type2, t_type3, mc_cores){
  type_1 = -sum(sapply(t_type0, function(t) (log(S_01(a, t)))))
  type_2 = -sum(unlist(mclapply(1:nrow(t_type1), function(i)(log(as.numeric(
    integrate(f0_S1, lower = t_type1[i,1], upper = t_type1[i,2], w = t_type1[i,3], a = a)[1]))),mc.cores = mc_cores), use.names = FALSE))
  type_3 = -sum(unlist(mclapply(1:nrow(t_type2), function(i){
    log(as.numeric(integrate(function(t){sapply(t, function(z){integrate(f0_f1_S2, 
                                                                         lower = max(t_type2[i,3]-z,0), upper = t_type2[i,4]-z, w = t_type2[i,5], t = z, a = a)$value})}, 
                             lower = t_type2[i,1], upper = t_type2[i,2])[1]))}, mc.cores = mc_cores), use.names = FALSE))
  type_4 = -sum(unlist(mclapply(1:nrow(t_type3), function(i){
    log(as.numeric(integrate(function(t){
      sapply(t, function(z){integrate(f0_f1_f2, 
                                      lower = max(t_type3[i,3]-z,0), upper = t_type3[i,4]-z, w = t_type3[i,5], t = z, a = a)$value})}, 
      lower = t_type3[i,1], upper = t_type3[i,2])[1]))}, mc.cores = mc_cores), use.names = FALSE))
  return(type_1 + type_2 + type_3 + type_4) 
}

