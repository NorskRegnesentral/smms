    unif3 = runif(1, min = 0, max = 1)
    S23 = function(T)
    {
      1-pweibull(T, param[5], param[6]) - unif3
    }
    T_23[i] = uniroot(S23, interval = c(1.e-14, 1e04), tol = 1e-9)$root
    t1 = 0.000001
    t2 = runif(1, t1, 1)
    t3 = runif(1, t1, 2)
    t4 = runif(1, t1, 5)
    t5 = runif(1, t1, 10)
    t6 = runif(1, t1, 12)
    t7 = runif(1, t1, 15)
    t8 = runif(1, t1, 18)
    t9 = runif(1, t1, 20)
    t10 = runif(1, t1, 22)
